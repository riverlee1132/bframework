function setProject(project) {
  const colors = colorPresets[project] || colorPresets.crmLight;

  document.body.setAttribute("data-project", project);

  for (const [key, value] of Object.entries(colors)) {
    document.documentElement.style.setProperty(`--sample-${key}`, value);
  }
}

// Set initial colors based on the existing body data-project attribute
const initialProject = document.body.getAttribute("data-project") || "crmLight";
setProject(initialProject);
